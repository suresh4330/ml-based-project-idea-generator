DATASETS = [
    "Kaggle Dataset", "Public Dataset", "Research Dataset",
    "Open Dataset", "Government Dataset", "Synthetic Dataset",
    "API-Collected Dataset", "Web-Scraped Dataset",
]

# ── 3. Difficulty — weighted toward Intermediate ───────────────────────────────────────────
DIFFICULTY_POOL = (
    ["Beginner"] * 25 + ["Intermediate"] * 45 + ["Advanced"] * 30
)


def generate_dataset(n_rows: int = 1000) -> pd.DataFrame:
    rows = []
    for _ in range(n_rows):
        proj = random.choice(PROJECTS)
        name, domain, description, tech, topic = proj
        difficulty = random.choice(DIFFICULTY_POOL)
        dataset = random.choice(DATASETS)
        rows.append({
            "project_name": name,
            "domain": domain,
            "description": description,
            "technologies": tech,
            "difficulty": difficulty,
            "dataset": dataset,
            "learning_topics": topic,
        })

    df = pd.DataFrame(rows)
    # Deduplicate on (project_name, domain, difficulty) so we keep variety
    df = df.drop_duplicates(subset=["project_name", "domain", "difficulty"]).reset_index(drop=True)
    # If after dedup we have fewer than n_rows, pad back up
    while len(df) < n_rows:
        extra = generate_dataset(n_rows - len(df))
        df = pd.concat([df, extra], ignore_index=True).drop_duplicates(
            subset=["project_name", "domain", "difficulty"]
        ).reset_index(drop=True)
        break  # one pass is enough
    return df


if __name__ == "__main__":
    df = generate_dataset(1000)
    df.to_csv("dataset.csv", index=False)
    print(f"✅ dataset.csv created with {len(df)} rows")
    print(f"   Unique projects  : {df['project_name'].nunique()}")
    print(f"   Unique domains   : {df['domain'].nunique()}")
    print(f"   Unique tech stacks: {df['technologies'].nunique()}")
    print(f"   Difficulty split : {df['difficulty'].value_counts().to_dict()}")